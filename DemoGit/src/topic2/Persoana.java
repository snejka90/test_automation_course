package topic2;

public class Persoana {

    public String prenume;
    public String nume;
    public Integer varsta;

    public Persoana(){
        this.prenume=prenume;
        this.nume=nume;
        this.varsta=varsta;
    }

    public String getPrenume(){
        return prenume;
    }
    public void setPrenume(){
        this.prenume=prenume;
    }
    public String getNume(){
        return nume;
    }
    public void setNume(){
        this.nume=nume;
    }
    public Integer getVarsta(){
        return varsta;
    }
    public void setVarsta(){
        this.varsta=varsta;
    }


}
